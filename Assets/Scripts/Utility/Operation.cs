namespace Watermelon_Game.Utility
{
    /// <summary>
    /// Represents various operations used in calculations
    /// </summary>
    internal enum Operation
    {
        /// <summary>
        /// Overwrites any existing value
        /// </summary>
        Set,
        /// <summary>
        /// Adds the value
        /// </summary>
        Add,
        /// <summary>
        /// Subtracts the value
        /// </summary>
        Subtract,
        /// <summary>
        /// Multiplies the value
        /// </summary>
        Multiply,
        /// <summary>
        /// Divides the value
        /// </summary>
        Divide
    }
}